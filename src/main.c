
#include "main.h"
#include "led_controller.h"
#include "led_game.h"
#include "led_pattern.h"
#include "stm32f4xx_hal.h"
#include <stm32f411xe.h>
#include <stm32f4xx_hal_adc.h>
#include <stm32f4xx_hal_def.h>
#include <stm32f4xx_hal_gpio.h>

/* DEFINES */
#define PATTERN_BUFFER_SIZE 100

/* VARIABLES */
ADC_HandleTypeDef hadc1;

// select the 4 LEDs present on our discovery board
struct LedPinMapping m_pinMapping[LED_COUNT] = {
    {.pin = LD4_Pin, .port = LD4_GPIO_Port},
    {.pin = LD3_Pin, .port = LD3_GPIO_Port},
    {.pin = LD5_Pin, .port = LD5_GPIO_Port},
    {.pin = LD6_Pin, .port = LD6_GPIO_Port}};

struct LedPatternStep m_patternBuffer[PATTERN_BUFFER_SIZE] = {};

uint16_t m_selectedPin = 0;
uint8_t m_buttonPressed = 0;

/* PROTOTYPES */

void SystemClock_Config(void);
void PeriphCommonClock_Config(void);
static void MX_GPIO_Init(void);
static void MX_ADC1_Init(void);
void Error_Handler();

/* main */

int main() {
  int err = 0;

  if (HAL_OK != HAL_Init()) {
    Error_Handler();
  }
  /* Configure the system clock */
  SystemClock_Config();

  /* Configure the peripherals common clocks */
  PeriphCommonClock_Config();

  /* Setup GPIO */
  MX_GPIO_Init();

  MX_ADC1_Init();

  struct GameSettings gameSettings = {m_pinMapping,
                                      .ledCount = LED_COUNT,
                                      .patternBuffer = m_patternBuffer,
                                      .bufferSize = sizeof(m_patternBuffer),
                                      .winningPin = LD6_Pin,
                                      0,
                                      250,
                                      10,
                                      .adc = &hadc1};

  /* Setup the LED game environment */
  LED_GAME_setup(&gameSettings, &m_buttonPressed);

  while (1) {

    err = LED_GAME_update(m_selectedPin);
    if (err != 0) {
      Error_Handler();
    }
  }
}

/**
 * @brief System Clock Configuration
 * @retval None
 */
void SystemClock_Config(void) {
  RCC_OscInitTypeDef RCC_OscInitStruct = {0};
  RCC_ClkInitTypeDef RCC_ClkInitStruct = {0};

  /** Configure the main internal regulator output voltage
   */
  __HAL_RCC_PWR_CLK_ENABLE();
  __HAL_PWR_VOLTAGESCALING_CONFIG(PWR_REGULATOR_VOLTAGE_SCALE1);

  /** Initializes the RCC Oscillators according to the specified parameters
   * in the RCC_OscInitTypeDef structure.
   */
  RCC_OscInitStruct.OscillatorType = RCC_OSCILLATORTYPE_HSE;
  RCC_OscInitStruct.HSEState = RCC_HSE_ON;
  RCC_OscInitStruct.PLL.PLLState = RCC_PLL_ON;
  RCC_OscInitStruct.PLL.PLLSource = RCC_PLLSOURCE_HSE;
  RCC_OscInitStruct.PLL.PLLM = 4;
  RCC_OscInitStruct.PLL.PLLN = 192;
  RCC_OscInitStruct.PLL.PLLP = RCC_PLLP_DIV4;
  RCC_OscInitStruct.PLL.PLLQ = 8;
  if (HAL_RCC_OscConfig(&RCC_OscInitStruct) != HAL_OK) {
    Error_Handler();
  }

  /** Initializes the CPU, AHB and APB buses clocks
   */
  RCC_ClkInitStruct.ClockType = RCC_CLOCKTYPE_HCLK | RCC_CLOCKTYPE_SYSCLK |
                                RCC_CLOCKTYPE_PCLK1 | RCC_CLOCKTYPE_PCLK2;
  RCC_ClkInitStruct.SYSCLKSource = RCC_SYSCLKSOURCE_PLLCLK;
  RCC_ClkInitStruct.AHBCLKDivider = RCC_SYSCLK_DIV1;
  RCC_ClkInitStruct.APB1CLKDivider = RCC_HCLK_DIV4;
  RCC_ClkInitStruct.APB2CLKDivider = RCC_HCLK_DIV1;

  if (HAL_RCC_ClockConfig(&RCC_ClkInitStruct, FLASH_LATENCY_3) != HAL_OK) {
    Error_Handler();
  }
}

/**
 * @brief Peripherals Common Clock Configuration
 * @retval None
 */
void PeriphCommonClock_Config(void) {
  RCC_PeriphCLKInitTypeDef PeriphClkInitStruct = {0};

  /** Initializes the peripherals clock
   */
  PeriphClkInitStruct.PeriphClockSelection = RCC_PERIPHCLK_I2S;
  PeriphClkInitStruct.PLLI2S.PLLI2SN = 200;
  PeriphClkInitStruct.PLLI2S.PLLI2SM = 5;
  PeriphClkInitStruct.PLLI2S.PLLI2SR = 2;
  if (HAL_RCCEx_PeriphCLKConfig(&PeriphClkInitStruct) != HAL_OK) {
    Error_Handler();
  }
}

